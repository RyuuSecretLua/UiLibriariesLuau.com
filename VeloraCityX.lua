					TweenService:Create(optBtn, TweenInfo.new(0.1), {BackgroundTransparency = 0.1, TextColor3 = PURPLE}):Play()
				end)
				optBtn.MouseLeave:Connect(function()
					TweenService:Create(optBtn, TweenInfo.new(0.1), {BackgroundTransparency = 0.4, TextColor3 = WHITE}):Play()
				end)
				optBtn.MouseButton1Click:Connect(function()
					selected = opt
					selLabel.Text = opt
					isDropOpen = false
					dropList.Visible = false
					if trackConn then trackConn:Disconnect() trackConn = nil end
					TweenService:Create(arrow, TweenInfo.new(0.2), {Rotation = 0}):Play()
					callback(opt)
				end)
