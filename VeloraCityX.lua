		function Tab:AddSlider(sliderConfig)
			sliderConfig = sliderConfig or {}
			local name = sliderConfig.Name or "Slider"
			local min = sliderConfig.Min or 0
			local max = sliderConfig.Max or 100
			local default = sliderConfig.Default or min
			local callback = sliderConfig.Callback or function() end
			local value = math.clamp(default, min, max)
			local row = makeRow(36)
			makeLabel(row, name)

			local sliderBox = Instance.new("Frame", row)
			sliderBox.Size = UDim2.new(0, 175, 0, 26)
			sliderBox.AnchorPoint = Vector2.new(1, 0.5)
			sliderBox.Position = UDim2.new(1, -8, 0.5, 0)
			sliderBox.BackgroundColor3 = Color3.fromRGB(80, 50, 140)
			sliderBox.BackgroundTransparency = 0.25
			sliderBox.BorderSizePixel = 0
			sliderBox.ZIndex = 21
			sliderBox.ClipsDescendants = false
			Instance.new("UICorner", sliderBox).CornerRadius = UDim.new(0, 6)

			local numLabel = Instance.new("TextLabel", sliderBox)
			numLabel.Size = UDim2.new(0, 38, 1, 0)
			numLabel.Position = UDim2.new(0, 6, 0, 0)
			numLabel.BackgroundTransparency = 1
			numLabel.Text = tostring(value)
			numLabel.TextColor3 = WHITE
			numLabel.Font = Enum.Font.GothamBold
			numLabel.TextSize = 12
			numLabel.TextXAlignment = Enum.TextXAlignment.Left
			numLabel.ZIndex = 23

			local trackBg = Instance.new("Frame", sliderBox)
			trackBg.Size = UDim2.new(1, -52, 0, 5)
			trackBg.Position = UDim2.new(0, 48, 0.5, -2)
			trackBg.BackgroundColor3 = Color3.fromRGB(30, 15, 55)
			trackBg.BorderSizePixel = 0
			trackBg.ZIndex = 22
			Instance.new("UICorner", trackBg).CornerRadius = UDim.new(1, 0)

			local trackFill = Instance.new("Frame", trackBg)
			trackFill.Size = UDim2.new((value - min) / (max - min), 0, 1, 0)
			trackFill.BackgroundColor3 = WHITE
			trackFill.BorderSizePixel = 0
			trackFill.ZIndex = 23
			Instance.new("UICorner", trackFill).CornerRadius = UDim.new(1, 0)

			local knob = Instance.new("Frame", trackBg)
			knob.Size = UDim2.new(0, 13, 0, 13)
			knob.AnchorPoint = Vector2.new(0.5, 0.5)
			knob.Position = UDim2.new((value - min) / (max - min), 0, 0.5, 0)
			knob.BackgroundColor3 = WHITE
			knob.BorderSizePixel = 0
			knob.ZIndex = 24
			Instance.new("UICorner", knob).CornerRadius = UDim.new(1, 0)

			local draggingSlider = false

			local function updateSlider(inputX)
				local abs = trackBg.AbsolutePosition
				local size = trackBg.AbsoluteSize
				local rel = math.clamp((inputX - abs.X) / size.X, 0, 1)
				value = math.floor(min + (max - min) * rel + 0.5)
				local pct = (value - min) / (max - min)
				trackFill.Size = UDim2.new(pct, 0, 1, 0)
				knob.Position = UDim2.new(pct, 0, 0.5, 0)
				numLabel.Text = tostring(value)
				callback(value)
			end

			local sliderBtn = Instance.new("TextButton", sliderBox)
			sliderBtn.Size = UDim2.new(1, 0, 1, 0)
			sliderBtn.BackgroundTransparency = 1
			sliderBtn.Text = ""
			sliderBtn.ZIndex = 25

			sliderBtn.MouseButton1Down:Connect(function(x, y)
				draggingSlider = true
				updateSlider(x)
			end)
			sliderBtn.MouseButton1Up:Connect(function()
				draggingSlider = false
			end)

			UIS.InputChanged:Connect(function(input)
				if draggingSlider and (input.UserInputType == Enum.UserInputType.MouseMovement or input.UserInputType == Enum.UserInputType.Touch) then
					updateSlider(input.Position.X)
				end
			end)
			UIS.InputEnded:Connect(function(input)
				if input.UserInputType == Enum.UserInputType.MouseButton1 or input.UserInputType == Enum.UserInputType.Touch then
					draggingSlider = false
				end
			end)
		end

